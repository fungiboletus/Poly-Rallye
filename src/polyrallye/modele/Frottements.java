package polyrallye.modele;

public class Frottements {
    public double resistanceAir;
    public double boue;
    public double sable;
    public double asphalt;
    public double gravier;
    public double terre;
    public double herbe;
    

    public Frottements(){
        
    }
    
    /**
     * @return the resistanceAir
     */
    public double getResistanceAir() {
        return resistanceAir;
    }


    /**
     * @return the boue
     */
    public double getBoue() {
        return boue;
    }


    /**
     * @return the sable
     */
    public double getSable() {
        return sable;
    }


    /**
     * @return the asphalt
     */
    public double getAsphalt() {
        return asphalt;
    }


    /**
     * @return the gravier
     */
    public double getGravier() {
        return gravier;
    }


    /**
     * @return the terre
     */
    public double getTerre() {
        return terre;
    }


    /**
     * @return the herbe
     */
    public double getHerbe() {
        return herbe;
    }
}
